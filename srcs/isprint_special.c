#include "ft_printf.h"

int		isprint_special(char c)
{
	if ((c >= 31 && c <= 127) || c == '\t' || c == '\r'
			|| c == '\v' || c == '\b' || c == '\f' || c == '\n')
		return (1);
	return (0);
}
